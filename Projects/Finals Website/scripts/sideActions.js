function openSide() {
	document.getElementById("sideNav").style.left = "0px";
	document.getElementById("bottomNav").style.left = "0px";
	document.getElementById("sideOpener").style.left = "290px";
}
function closeSide() {
	document.getElementById("sideNav").style.left = "-300px";
	document.getElementById("bottomNav").style.left = "-300px";
	document.getElementById("sideOpener").style.left = "-290px";
}