var light = document.getElementById('lightbulb1');
light.style.filter = "grayscale(" + 100 + "%)";
function turnOn() {
	if(light.style.filter != "grayscale(" + 100 + "%)"){
        light.setAttribute("style","filter:grayscale(" + 100 + "%)");
	}else{
		light.setAttribute("style","filter:grayscale(" + 0 + "%)");
	}
}
